	<p>Following in the footsteps of the highly developed and successful Teenage Cancer Trust – Teen and Young Adult Cancer Program out of the UK,  Teen Cancer America is designed to help hospitals and healthcare professionals bridge the gap between pediatric and adult oncology care. Teen Cancer America aims to educate and support hospitals and outpatient facilities in the development of specialized units for this age group.  The units are a place just for teens and young adults where the cancer experience is understood and the importance of living life through the cancer journey is nourished.</p>
	<p>Teen Cancer America’s work supports the coming together of physicians and allied healthcare professionals in both pediatric and adult oncology so that the teen and young adult cancer patient care becomes closer to where it needs to be.  Age targeted care for this population is necessary for medical and appropriate psychosocial development of this unique age group. Research shows that some cancers that target this particular age group have not improved in over thirty years while improvements have been made in both pediatric and adult cancers alike.  Teens and young adults with cancer are long overdue for an upgrade and Teen Cancer America 
can hopefully light the fire in America’s health systems.</p>
